        if (randomChance > 0.8) { // 20% chance to launch a product
            console.log("📈 CEO ANALYZING MARKET TRENDS...");
            
            const opportunity = TRENDS[Math.floor(Math.random() * TRENDS.length)];
            
            // CREATE PRODUCT (Virtual Agent)
            const newAgent = {
                name: opportunity.name,
                price_usd: opportunity.price,
                description: `Auto-generated by Nehira Core based on high demand.`,
                status: 'LISTED'
            };

            console.log(`💰 OPPORTUNITY SPOTTED: Creating '${newAgent.name}' for $${newAgent.price_usd}`);
            
            // Simulate Sale
            const saleChance = Math.random();
            if (saleChance > 0.5) {
                console.log(`💲 SALE ALERT! Sold '${newAgent.name}'. Revenue: +$${newAgent.price_usd}`);
                // TODO: Add to Total Revenue Table in Database
            }
        }
        await new Promise(r => setTimeout(r, 10000));
